import mongoose from 'mongoose';

const userSchema=new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    contact:{
        type:String,
        required:true,
        unique:true,
        trim:true,
        lowercase:true
    },
    email:{
        type:String,
        required:true,
        unique:true,
        trim:true,
        lowercase:true
    },
    phone:{
        type:String,
        required:true,
        unique:true,
        trim:true
    },
    password:{
        type:String,
        required:true
    },
    address:{
        type:String,
        required:true
    },
    wishlist:[{
        type:mongoose.Schema.Types.ObjectId,
        ref:"Product"
    }],
    orders:[{
        type:mongoose.Schema.Types.ObjectId,
        ref:"Product"
    }],
    addedProducts:[{
        type:mongoose.Schema.Types.ObjectId,
        ref:"Product"
    }],
    responses:[{
        productId:{
            type:mongoose.Schema.Types.ObjectId,
            ref:"Product",
            required:true
        },
        from:{
            type:mongoose.Schema.Types.ObjectId,
            ref:"User",
            required:true
        },
        fromName:{
            type:String,
            default:""
        },
        fromContact:{
            type:String,
            default:""
        },
        message:{
            type:String,
            required:true
        },
        bidValue:{
            type:Number,
            default:null
        },
        receivedAt:{
            type:Date,
            default:Date.now
        }
    }],
    pastDeals:[{
        productId:{
            type:mongoose.Schema.Types.ObjectId,
            ref:"Product",
            required:true
        },
        withUser:{
            type:mongoose.Schema.Types.ObjectId,
            ref:"User",
            required:true
        },
        dealValue:{
            type:Number,
            required:true
        },
        dealDate:{
            type:Date,
            default:Date.now
        }
    }],
    acceptedDeals:[{
        productId:{
            type:mongoose.Schema.Types.ObjectId,
            ref:"Product",
            required:true
        },
        withUser:{
            type:mongoose.Schema.Types.ObjectId,
            ref:"User",
            required:true
        },
        dealValue:{
            type:Number,
            required:true
        },
        acceptedAt:{
            type:Date,
            default:Date.now
        }
    }],
},{timestamps:true});

const User=mongoose.model("User",userSchema);

export default User;